def detect_yassir(text):
    """Распознаёт обращение к Ясиру: ясир, yassir, yasir, ясыр и т.п. Возвращает вопрос или None"""
    t = text.strip()
    low = t.lower()
    variants = ["ясир", "ясыр", "yassir", "yasir", "yassır", "яссир"]
    for v in variants:
        if low.startswith(v):
            question = t[len(v):].lstrip(" ,!:-—?")
            return question
    return None

