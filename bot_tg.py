                    with db() as c:
                        rows = c.execute(
                            'SELECT user_id FROM unregistered_members WHERE chat_id=? AND julianday("now") - julianday(joined_date) >= 14',
                            (group["chat_id"],)).fetchall()
                    for row in rows:
                        uid = row["user_id"]
                        stud = get_student_by_phone(uid, group["id"])
                        if not stud:
                            try:
                                await tg_call("banChatMember", {"chat_id": group["chat_id"], "user_id": uid})
                                await tg_call("unbanChatMember", {"chat_id": group["chat_id"], "user_id": uid})
                            except Exception as e:
                                log.warning("Kick unreg: %s", e)
                        with db() as c:
                            c.execute("DELETE FROM unregistered_members WHERE user_id=? AND chat_id=?", (uid, group["chat_id"]))

