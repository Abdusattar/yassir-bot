    """Перевод сообщения для студента на язык группы."""
    table = TR.get(key, {})
    text = table.get(lang) or table.get("ru") or ""
    # Если значение — список, выбираем случайный вариант
    if isinstance(text, list):
        text = random.choice(text)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except Exception:
            pass
    return text



