async def ask_claude(prompt, system="", retries=3):
    """Запрос к Claude Haiku с повторными попытками при сбое."""
    headers = {
        "x-api-key": CLAUDE_API_KEY,
        "anthropic-version": "2023-06-01",
        "content-type": "application/json"
    }
    data = {
        "model": CLAUDE_MODEL,
        "max_tokens": 2048,
        "messages": [{"role": "user", "content": prompt}]
    }
    if system:
        data["system"] = system
    for attempt in range(retries):
        try:
            async with aiohttp.ClientSession() as session:
                async with session.post(
                    CLAUDE_URL,
                    headers=headers,
                    json=data,
                    timeout=aiohttp.ClientTimeout(total=45)
                ) as resp:
                    result = await resp.json()
                    if "error" in result:
                        log.warning("Claude error (attempt %d): %s", attempt+1, result.get("error"))
                        if attempt < retries - 1:
                            await asyncio.sleep(3)
                            continue
                        return None
                    return result["content"][0]["text"]
        except Exception as e:
            log.error("Claude error (attempt %d): %s", attempt+1, e)
            if attempt < retries - 1:
                await asyncio.sleep(3)
    return None

