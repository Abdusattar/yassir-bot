async def process_chat_history(chat_id, group_id, group_tasks, count=100):
    """Обработать историю чата: засчитать пропущенные отчёты. Возвращает (кол-во, список имён)."""
    history = await get_chat_history(chat_id, count)
    if not history or not isinstance(history, list):
        return 0, []
    counted = 0
    counted_names = set()
    tz = pytz.timezone(TZ)
    for msg in reversed(history):  # от старых к новым
        try:
            msg_text = (msg.get("textMessage")
                        or msg.get("extendedTextMessage", {}).get("text", "")
                        or msg.get("caption", "")
                        or msg.get("fileMessageData", {}).get("caption", ""))
            sender = msg.get("senderId", "")
            ts = msg.get("timestamp", 0)
            if not msg_text or not sender or msg.get("type") != "incoming":
                continue
            msg_phone = extract_phone(sender)
            if msg_phone in ADMIN_PHONES:
                continue
            stud = find_by_phone(msg_phone, group_id)
            if not stud:
                continue
            msg_date = datetime.fromtimestamp(ts, tz).date().isoformat() if ts else get_date()
            classified = await gemini_classify_message(msg_text, group_tasks)
            if not classified or "report" not in classified.get("type", ""):
                continue
            tasks = classified["tasks"]
            if not tasks:
                continue
            with db() as c:
                prev = c.execute("SELECT * FROM reports WHERE sid=? AND date=?", (stud["id"], msg_date)).fetchone()
            # Проверяем — добавится ли что-то новое (чтобы не объявлять уже засчитанное)
            already = set()
            if prev:
                already = {k for k in group_tasks if prev[k]}
            new_tasks = {k for k in tasks if tasks[k]} - already
            merged = dict(tasks)
            if prev:
                for k in group_tasks:
                    if prev[k]:
                        merged[k] = True
            save_report(stud["id"], group_id, msg_date, merged)
            if new_tasks:  # засчитали что-то новое
                counted += 1
                counted_names.add(stud["name"])
                # Проверяем знания через ИИ (таджвид/нахв/муфрадат) если есть содержание
                if len(msg_text.strip()) > 15:
                    checks = []
                    if tasks.get("j"):
                        checks.append("таджвид")
                    if tasks.get("n"):
                        checks.append("грамматику (нахв)")
                    if tasks.get("t"):
                        checks.append("муфрадат (написание арабских слов и перевод)")
                    if checks:
                        gr = get_group(chat_id)
                        gtitle = gr["title"] if gr else ""
                        asyncio.create_task(_verify_and_reply(chat_id, msg_text, gtitle, msg_phone, group_id, stud["name"], checks))
        except Exception as e:
            log.error("history msg error: %s", e)
            continue
    return counted, sorted(counted_names)

