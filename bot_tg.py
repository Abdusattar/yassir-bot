                    bonus_names = []
                    for s in get_students(group["id"]):
                        streak = get_streak_days(s["id"])
                        if streak > 0 and streak % 7 == 0:
                            # Уникальный бонус за этот рубеж (по номеру недели серии)
                            week_num = streak // 7
                            reason = "streak_week_" + str(week_num)
                            # Проверяем не давали ли уже за этот рубеж
                            with db() as c:
                                exists = c.execute(
                                    "SELECT 1 FROM bonus_points WHERE sid=? AND reason=?",
                                    (s["id"], reason)).fetchone()
                            if not exists:
                                add_bonus(s["id"], group["id"], today.isoformat(), 5, reason)
                                bonus_names.append((s["name"], streak))
                    if bonus_names:
                        await send_message(group["chat_id"],
                            "🌟 Бонус +5 очков за серию без пропусков:\n" +
                            "\n".join("• " + n + " — " + str(d) + " дней подряд!" for n, d in bonus_names))

