async def send_message(chat_id, text):
    """Отправить сообщение в чат Telegram (группа или личка).
    chat_id приходит как строка — приводим к int если это число."""
    cid = chat_id
    try:
        cid = int(str(chat_id))
    except (ValueError, TypeError):
        cid = chat_id
    # Telegram режет сообщения длиннее 4096 символов — разбиваем.
    parts = []
    t = text or ""
    while len(t) > 4096:
        cut = t.rfind("\n", 0, 4096)
        if cut <= 0:
            cut = 4096
        parts.append(t[:cut])
        t = t[cut:]
    parts.append(t)
    last = None
    for p in parts:
        if not p:
            continue
        last = await tg_call("sendMessage", {"chat_id": cid, "text": p})
        await asyncio.sleep(0.05)
    return last

